console.log("1 + 2 * 3 =", 1 + 2 * 3); // 곱셈이 먼저: 1 + (2*3) = 7
console.log("(1 + 2) * 3 =", (1 + 2) * 3); // 괄호가 먼저: (1+2)*3 = 9
console.log("4 / 2 + 8 =", 4 / 2 + 8); // 나눗셈이 먼저: (4/2)+8 = 10
console.log("4 / (2 + 8) =", 4 / (2 + 8)); // 괄호가 먼저: 4/(2+8) = 0.4
console.log("2 ** 3 * 2 =", 2 ** 3 * 2); // 거듭제곱이 먼저: (2**3)*2 = 16
console.log("10 % 3 + 2 =", 10 % 3 + 2); // 나머지 연산이 먼저: (10%3)+2 = 3
console.log("true && false || true =", true && false || true); // &&가 먼저: (true&&false)||true = true
console.log("false || true && false =", false || (true && false)); // &&가 먼저: false||(true&&false) = false

